export const generateRandomMsg = (length) => {
  let result = "";
  const characters =
    "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
  const charactersLength = characters.length;
  let counter = 0;
  while (counter < length) {
    result += characters.charAt(Math.floor(Math.random() * charactersLength));
    counter += 1;
  }
  return result;
};

export const prime = (n) => {
  var primes = [2];
  var x = 3;
  var count = 0;
  do {
    for (var y = 2; y < x; y++) {
      if (x % y === 0) {
        count++;
      }
    }
    if (count === 0) {
      primes.push(x);
      x++;
    } else {
      x++;
      count = 0;
    }
  } while (primes.length < n);
  return primes[primes.length - 1];
};

export const formatNumber = (n) => {
  if (n < 1e3) return n;
  if (n >= 1e3 && n < 1e6) return +(n / 1e3).toFixed(1) + "K";
  if (n >= 1e6 && n < 1e9) return +(n / 1e6).toFixed(1) + "M";
  if (n >= 1e9 && n < 1e12) return +(n / 1e9).toFixed(1) + "B";
  if (n >= 1e12) return +(n / 1e12).toFixed(1) + "T";
};

export const formatTime = (ytDateTimeString) => {
  const targetTimestamp = new Date(ytDateTimeString);
  const now = new Date();

  // Calculate the time difference in milliseconds
  const difference = now.getTime() - targetTimestamp.getTime();

  // Calculate the time difference in seconds, minutes, hours, days, months and years
  const seconds = Math.floor(difference / 1000);
  const minutes = Math.floor(seconds / 60);
  const hours = Math.floor(minutes / 60);
  const days = Math.floor(hours / 24);
  const months = Math.floor(days / 30);
  const years = Math.floor(months / 12);

  // Determine the appropriate time unit to display
  let timeUnit;
  let timeValue;

  if (years > 0) {
    timeUnit = "year";
    timeValue = years;
  } else if (months > 0) {
    timeUnit = "month";
    timeValue = months;
  } else if (days > 0) {
    timeUnit = "day";
    timeValue = days;
  } else if (hours > 0) {
    timeUnit = "hour";
    timeValue = hours;
  } else if (minutes > 0) {
    timeUnit = "minute";
    timeValue = minutes;
  } else {
    timeUnit = "second";
    timeValue = seconds;
  }

  // Format the output
  const output = `${timeValue} ${timeUnit}${timeValue === 1 ? "" : "s"} ago`;

  return output;
};

export function useHorizontalScroll() {
  const elRef = useRef();
  useEffect(() => {
    const el = elRef.current;
    if (el) {
      const onWheel = (e) => {
        if (e.deltaY == 0) return;
        e.preventDefault();
        el.scrollTo({
          left: el.scrollLeft + e.deltaY,
          behavior: "smooth",
        });
      };
      el.addEventListener("wheel", onWheel);
      return () => el.removeEventListener("wheel", onWheel);
    }
  }, []);
  return elRef;
}

export function horizontalScroll(event) {
  const delta = Math.max(
    -1,
    Math.min(1, event.nativeEvent.wheelDelta || -event.nativeEvent.detail)
  );
  event.currentTarget.scrollLeft -= delta * 10;
  event.preventDefault();
}
